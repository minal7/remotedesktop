import CoreGraphics

/// Translates USB HID usage codes (page 0x07) — the wire format for
/// keyboard events — into macOS Carbon virtual keycodes used by
/// `CGEvent(keyboardEventSource:virtualKey:keyDown:)`.
///
/// Covers letters, digits, common punctuation, arrows, nav, F1–F12,
/// and modifiers. Deadkeys and numeric keypad aren't mapped yet —
/// they fall through to `nil` and the injector ignores them (clients
/// can work around by sending the composed `text` message instead).
enum HIDUsage {
    static func toMacKeyCode(_ usage: Int) -> CGKeyCode? {
        map[usage].map { CGKeyCode($0) }
    }

    private static let map: [Int: Int] = [
        // Letters: HID 0x04..0x1D → kVK_ANSI_*
        0x04: 0x00, 0x05: 0x0B, 0x06: 0x08, 0x07: 0x02,   // A B C D
        0x08: 0x0E, 0x09: 0x03, 0x0A: 0x05, 0x0B: 0x04,   // E F G H
        0x0C: 0x22, 0x0D: 0x26, 0x0E: 0x28, 0x0F: 0x25,   // I J K L
        0x10: 0x2E, 0x11: 0x2D, 0x12: 0x1F, 0x13: 0x23,   // M N O P
        0x14: 0x0C, 0x15: 0x0F, 0x16: 0x01, 0x17: 0x11,   // Q R S T
        0x18: 0x20, 0x19: 0x09, 0x1A: 0x0D, 0x1B: 0x07,   // U V W X
        0x1C: 0x10, 0x1D: 0x06,                            // Y Z

        // Digits (top row): HID 0x1E..0x27 → kVK_ANSI_1..0
        0x1E: 0x12, 0x1F: 0x13, 0x20: 0x14, 0x21: 0x15, 0x22: 0x17,
        0x23: 0x16, 0x24: 0x1A, 0x25: 0x1C, 0x26: 0x19, 0x27: 0x1D,

        // Control / editing
        0x28: 0x24,   // Return
        0x29: 0x35,   // Escape
        0x2A: 0x33,   // Delete / Backspace
        0x2B: 0x30,   // Tab
        0x2C: 0x31,   // Space

        // Punctuation
        0x2D: 0x1B,   // -
        0x2E: 0x18,   // =
        0x2F: 0x21,   // [
        0x30: 0x1E,   // ]
        0x31: 0x2A,   // \
        0x33: 0x29,   // ;
        0x34: 0x27,   // '
        0x35: 0x32,   // `
        0x36: 0x2B,   // ,
        0x37: 0x2F,   // .
        0x38: 0x2C,   // /
        0x39: 0x39,   // Caps Lock

        // Function row
        0x3A: 0x7A, 0x3B: 0x78, 0x3C: 0x63, 0x3D: 0x76,   // F1-F4
        0x3E: 0x60, 0x3F: 0x61, 0x40: 0x62, 0x41: 0x64,   // F5-F8
        0x42: 0x65, 0x43: 0x6D, 0x44: 0x67, 0x45: 0x6F,   // F9-F12

        // Nav
        0x4A: 0x73,   // Home
        0x4B: 0x74,   // Page Up
        0x4C: 0x75,   // Forward Delete
        0x4D: 0x77,   // End
        0x4E: 0x79,   // Page Down
        0x4F: 0x7C,   // Right
        0x50: 0x7B,   // Left
        0x51: 0x7D,   // Down
        0x52: 0x7E,   // Up

        // Modifiers
        0xE0: 0x3B,   // L Ctrl
        0xE1: 0x38,   // L Shift
        0xE2: 0x3A,   // L Option
        0xE3: 0x37,   // L Cmd
        0xE4: 0x3E,   // R Ctrl
        0xE5: 0x3C,   // R Shift
        0xE6: 0x3D,   // R Option
        0xE7: 0x36,   // R Cmd
    ]
}
